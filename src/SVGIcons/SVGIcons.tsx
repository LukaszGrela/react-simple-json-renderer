export interface IProps {
  /**
   * The `IconTypesEnum` string
   */
  icon: TIconType;
  color?: TIconColor;
  title?: string;
  className?: string;

  viewBox?: string;
  id?: string;

  /**
   * When true the size is in form of 1em and fontSize is used
   */
  emBased?: boolean;

  /**
   * Get element from external SVG
   */
  href?: string;
}
export const guardISVGIconProps = (test: unknown): test is IProps => {
  return Object.hasOwn(test as object, 'icon');
};

const SVGIcon: React.FC<IProps> = ({
  icon,
  color,
  title,
  className,
  id,
  href,
  emBased = false,
  viewBox = '0 0 64 64',
}: IProps): JSX.Element => (
  <svg
    id={id}
    role={title ? 'img' : undefined}
    aria-hidden={title ? undefined : true}
    className={`SVGIcon${color ? ` SVGIcon_${color}` : ''}${className ? ` ${className}` : ''}${
      emBased ? ' SVGIcon-em-based' : ''
    }`}
    xmlns='http://www.w3.org/2000/svg'
    viewBox={viewBox}
  >
    {process.env.NODE_ENV === 'development' && `<!-- ${icon} -->`}
    {!href && showIcon(icon as IconTypesEnum)}
    {href && <use href={href} />}
    {title && <title>{title}</title>}
  </svg>
);

export default SVGIcon;
